package myPkg1;

public class CircleImpl implements Circle {

	
	Shape pointX;
	int x;
	int y;
	int radius;
	
	public CircleImpl() {
		System.out.println("CircleImpl 생성자");
	}
	
	public Shape getPointX() {
		return pointX;
	}
	public void setPointX(Shape pointX) {
		this.pointX = pointX;
	}
	@Override
	public int getX() {
		return x;
	}

	@Override
	public int getY() {
		return y;
	}

	@Override
	public String make() {
		return pointX.make();
	}
	
}
